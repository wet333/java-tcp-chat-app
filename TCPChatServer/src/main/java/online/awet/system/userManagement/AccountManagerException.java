package online.awet.system.userManagement;

/**
 * The {@code AccountManagerException} class represents exceptions specific to account management operations.
 */
public class AccountManagerException extends Exception {

  /**
   * Constructs a new {@code AccountManagerException} with the specified message.
   *
   * @param message the detailed error information.
   */
  public AccountManagerException(String message) {
    super(message);
  }
}
